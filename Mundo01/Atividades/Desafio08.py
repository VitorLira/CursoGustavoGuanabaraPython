# Escreva um programa que leia um valor em metros e o exiba convertidos em centimetros e milímetros.

met = float(input('Digite o valor em metros para ver o mesmo em centimetros e milimetros: '))
print(f'O valor em metros é {met}, em centimetros é {met*100:.2f}, e em milímetros é {met*1000:.2f} .')
